  group_by(
    cutoff,
    condition
  ) %>%
  summarise(
    n_samples = n(),
    
    total_double_positive =
      sum(n_double_positive),
    
    mean_per_sample =
      mean(n_double_positive),
    
    median_per_sample =
      median(n_double_positive),
    
    Q1 =
      quantile(
        n_double_positive,
        0.25
      ),
    
    Q3 =
      quantile(
        n_double_positive,
        0.75
      ),
    
    min_per_sample =
      min(n_double_positive),
    
    max_per_sample =
      max(n_double_positive),
    
    n_samples_with_zero =
      sum(
        n_double_positive == 0
      ),
    
    .groups = "drop"
  )


