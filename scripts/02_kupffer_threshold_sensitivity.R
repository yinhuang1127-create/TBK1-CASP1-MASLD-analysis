      signif(Q1, 3),
      "–",
      signif(Q3, 3)
    ),
    
    Range = paste0(
      min_per_sample,
      "–",
      max_per_sample
    ),
    
    samples_with_zero = paste0(
      n_samples_with_zero,
      "/",
      n_samples
    )
  ) %>%
  arrange(
    cutoff,
    condition
  ) %>%
  transmute(
    Cutoff =
      as.character(cutoff),
    
    Condition =
      as.character(condition),
    
    `Total double-positive spots` =
      total_double_positive,
    
    `Mean spots/sample` =
      round(
        mean_per_sample,
        2
      ),
    
    `Median spots/sample` =
      round(
        median_per_sample,
        2
      ),
    
    IQR =
      IQR,
    
    Range =
      Range,
    
    `Samples with zero DP spots` =
      samples_with_zero,
    
    `Between-condition P` =
      signif(
        `Between-condition P`,
        4
      )
  )


