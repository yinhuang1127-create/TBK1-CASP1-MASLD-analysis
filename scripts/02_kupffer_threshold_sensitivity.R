############################################################
## 8. Safe Fisher exact test
############################################################

safe_fisher_or <- function(
    df,
    group_col,
    outcome_col,
    ref_group,
    target_group
) {
  
  df_sub <- df %>%
    filter(
      .data[[group_col]] %in%
        c(
          ref_group,
          target_group
        )
    )
  
  n_ref <- sum(
    df_sub[[group_col]] == ref_group,
    na.rm = TRUE
  )
  
  n_target <- sum(
    df_sub[[group_col]] == target_group,
    na.rm = TRUE
  )
  
  if (n_ref == 0 || n_target == 0) {
    return(
      tibble(
        OR = NA_real_,
        CI_low = NA_real_,
        CI_high = NA_real_,
        p_value = NA_real_,
        n_ref = n_ref,
        n_target = n_target
      )
    )
  }
  
  tab <- table(
    factor(
      df_sub[[group_col]],
      levels = c(
        ref_group,
        target_group
      )
    ),
    factor(
      df_sub[[outcome_col]],
      levels = c(
        FALSE,
        TRUE
      )
    )
  )
  
  if (!all(dim(tab) == c(2, 2))) {
    return(
      tibble(
        OR = NA_real_,
        CI_low = NA_real_,
        CI_high = NA_real_,
        p_value = NA_real_,
        n_ref = n_ref,
        n_target = n_target
      )
    )
  }
  
  if (
    sum(tab[, "FALSE"]) == 0 ||
    sum(tab[, "TRUE"]) == 0
  ) {
    return(
      tibble(
        OR = NA_real_,
        CI_low = NA_real_,
        CI_high = NA_real_,
        p_value = NA_real_,
        n_ref = n_ref,
        n_target = n_target
      )
    )
  }
  
  ft <- tryCatch(
    fisher.test(tab),
    error = function(e) NULL
  )
  
  if (is.null(ft)) {
    return(
      tibble(
        OR = NA_real_,
        CI_low = NA_real_,
        CI_high = NA_real_,
        p_value = NA_real_,
        n_ref = n_ref,
        n_target = n_target
      )
    )
  }
  
  tibble(
    OR = as.numeric(
      ft$estimate
    ),
    CI_low = as.numeric(
      ft$conf.int[1]
    ),
    CI_high = as.numeric(
      ft$conf.int[2]
    ),
    p_value = ft$p.value,
    n_ref = n_ref,
    n_target = n_target
  )
}


############################################################
## 9. Safe Wilcoxon rank-sum test
############################################################

safe_wilcox <- function(
    df,
    value_col,
    group_col = "condition"
) {
  
  df_test <- df %>%
    filter(
      !is.na(.data[[value_col]]),
      !is.na(.data[[group_col]])
    )
  
  wt <- tryCatch(
    suppressWarnings(
      wilcox.test(
        df_test[[value_col]] ~
          df_test[[group_col]],
        exact = TRUE
      )
    ),
    error = function(e) NULL
  )
  
  if (is.null(wt)) {
    wt <- tryCatch(
      suppressWarnings(
        wilcox.test(
          df_test[[value_col]] ~
            df_test[[group_col]],
          exact = FALSE
        )
      ),
      error = function(e) NULL
    )
  }
  
  if (is.null(wt)) {
    return(
      tibble(
        statistic = NA_real_,
        p_value = NA_real_
      )
    )
  }
  
  tibble(
    statistic = as.numeric(
      wt$statistic
    ),
    p_value = wt$p.value
  )
}


############################################################
## 10. Run one threshold
############################################################

run_threshold_analysis <- function(
    base_df,
    cutoff_label,
    quantile_prob
) {
  
  ##########################################################
  ## A. Define Kupffer-high independently per sample
  ##########################################################
  
  df_thr <- base_df %>%
    group_by(
      sample
    ) %>%
    mutate(
      kup_cut = quantile(
        Kupffer1,
        probs = quantile_prob,
        na.rm = TRUE,
        names = FALSE
      ),
      Kupffer_region_sens = ifelse(
        Kupffer1 >= kup_cut,
        "Kupffer-high",
        "Other"
      )
    ) %>%
    ungroup() %>%
    mutate(
      tbk_casp_state = case_when(
        Tbk1 == 0 &
          Casp1 == 0 ~
          "Tbk1-Casp1-",
        
        Tbk1 == 0 &
          Casp1 > 0 ~
          "Tbk1-Casp1+",
        
        Tbk1 > 0 &
          Casp1 == 0 ~
          "Tbk1+Casp1-",
        
        Tbk1 > 0 &
          Casp1 > 0 ~
          "Tbk1+Casp1+",
        
        TRUE ~ NA_character_
      ),
      
      Aim2_pos = Aim2 > 0,
      Casp4_pos = Casp4 > 0,
      Nlrp3_pos = Nlrp3 > 0
    )
  
  
  ##########################################################
  ## B. Supplementary Table 2 source
  ## Sample-level DP representation
  ##########################################################
  
  double_positive_per_sample <- df_thr %>%
    filter(
      Kupffer_region_sens ==
        "Kupffer-high"
    ) %>%
    group_by(
      condition,
      sample
    ) %>%
    summarise(
      n_double_positive = sum(
        tbk_casp_state ==
          "Tbk1+Casp1+",
        na.rm = TRUE
      ),
      n_kupffer_high = n(),
      frac_double_in_kupffer =
        n_double_positive /
        n_kupffer_high,
      .groups = "drop"
    ) %>%
    mutate(
      cutoff = cutoff_label
    )
  
  
  ##########################################################
  ## Chow vs CDAA-HFD test
  ##########################################################
  
  wilcox_result <- safe_wilcox(
    double_positive_per_sample,
    value_col =
      "frac_double_in_kupffer",
    group_col =
      "condition"
  ) %>%
    mutate(
      cutoff = cutoff_label
    )
  
  
  ##########################################################
  ## C. Supplementary Table 3 source
  ## Sensor enrichment:
  ## DP vs DN
  ##########################################################
  
  sensor_compare_df <- df_thr %>%
    filter(
      condition ==
        "CDAA-HFD",
      Kupffer_region_sens ==
        "Kupffer-high",
      tbk_casp_state %in%
        c(
          "Tbk1-Casp1-",
          "Tbk1+Casp1+"
        )
    )
  
  
  sensor_results <- bind_rows(
    
    safe_fisher_or(
      df = sensor_compare_df,
      group_col = "tbk_casp_state",
      outcome_col = "Aim2_pos",
      ref_group = "Tbk1-Casp1-",
      target_group = "Tbk1+Casp1+"
    ) %>%
      mutate(
        gene = "Aim2"
      ),
    
    safe_fisher_or(
      df = sensor_compare_df,
      group_col = "tbk_casp_state",
      outcome_col = "Casp4_pos",
      ref_group = "Tbk1-Casp1-",
      target_group = "Tbk1+Casp1+"
    ) %>%
      mutate(
        gene = "Casp4"
      ),
    
    safe_fisher_or(
      df = sensor_compare_df,
      group_col = "tbk_casp_state",
      outcome_col = "Nlrp3_pos",
      ref_group = "Tbk1-Casp1-",
      target_group = "Tbk1+Casp1+"
    ) %>%
      mutate(
        gene = "Nlrp3"
      )
  ) %>%
    mutate(
      cutoff = cutoff_label,
      p_adj_BH = p.adjust(
        p_value,
        method = "BH"
      )
    )
  
  
  ##########################################################
  ## D. Supplementary Table 4 source
  ## Casp4 coupling across states
  ##########################################################
  
  casp4_source_df <- df_thr %>%
    filter(
      condition ==
        "CDAA-HFD",
      Kupffer_region_sens ==
        "Kupffer-high"
    )
  
  
  casp4_coupling_results <- bind_rows(
    
    safe_fisher_or(
      df = casp4_source_df,
      group_col = "tbk_casp_state",
      outcome_col = "Casp4_pos",
      ref_group = "Tbk1-Casp1-",
      target_group = "Tbk1-Casp1+"
    ) %>%
      mutate(
        comparison =
          "Tbk1-Casp1+ vs Tbk1-Casp1-"
      ),
    
    safe_fisher_or(
      df = casp4_source_df,
      group_col = "tbk_casp_state",
      outcome_col = "Casp4_pos",
      ref_group = "Tbk1-Casp1-",
      target_group = "Tbk1+Casp1-"
    ) %>%
      mutate(
        comparison =
          "Tbk1+Casp1- vs Tbk1-Casp1-"
      ),
    
    safe_fisher_or(
      df = casp4_source_df,
      group_col = "tbk_casp_state",
      outcome_col = "Casp4_pos",
      ref_group = "Tbk1-Casp1-",
      target_group = "Tbk1+Casp1+"
    ) %>%
      mutate(
        comparison =
          "Tbk1+Casp1+ vs Tbk1-Casp1-"
      )
  ) %>%
    mutate(
      cutoff = cutoff_label,
      p_adj_BH = p.adjust(
        p_value,
        method = "BH"
      )
    )
  
  
  ##########################################################
  ## Return only objects needed for Tables 2–4
  ##########################################################
  
  list(
    double_positive_per_sample =
      double_positive_per_sample,
    
    wilcox_result =
      wilcox_result,
    
    sensor_results =
      sensor_results,
    
    casp4_coupling_results =
      casp4_coupling_results
  )
}


